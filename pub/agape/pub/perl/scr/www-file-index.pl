				# ------------------------------
if ($#ARGV<1){			# help
    print  "goal: $scrGoal\n";
    print  "use:  '$scrName files*'\n";
    exit;}
				# initialise variables
$#fileIn=0;
$fileOut="index.html";
$fhout="FHOUT";
				# ------------------------------
				# read command line
foreach $arg (@ARGV){
    if (-e $arg)                { 
	push(@fileIn,$arg); }
    else {
	print "*** wrong command line arg '$arg'\n";
	exit;}}

				# ------------------------------
				# (1) read file(s)
				# ------------------------------
foreach $fileIn (@fileIn){
    if (! -e $fileIn){print "-*- WARN $scrName: no fileIn=$fileIn\n";
		      next;}
    print "--- $scrName: working on '$fileIn'\n";
    $tmp=`ls -l $fileIn`; $tmp=~s/\n//g;
    $res{$fileIn}=$tmp;
}
				# ------------------------------
				# (3) write output
				# ------------------------------

open($fhout,">".$fileOut) || die "*** $scrName ERROR creating file $fileOut";

print $fhout
    "<HTML>\n",
    "<HEAD><TITLE>index of directory</TITLE></HEAD>\n",
    "<BODY>\n",
    "<STRONG>Index of directory</STRONG>\n",
#    "<TABLE BORDER CELLPADDING=3>\n";
    "<PRE>\n";


foreach $fileIn (@fileIn){
    next if ($fileIn =~/index\.html/);
    next if (! defined $res{$fileIn});
    $link="<A HREF=\"$fileIn\">$fileIn</A>";
    $res{$fileIn}=~s/$fileIn/$link/;
    print $fhout  $res{$fileIn},"\n";
    next;
    print $fhout "<TR>";
    foreach $kwd (split(/,/,$res{$fileIn})){
	print $fhout "<TD>$kwd</TD>";
    }
    print $fhout "</TR>\n";
}
	
print $fhout
#    "</TABLE>\n",
    "</PRE>\n",
    "</BODY>\n",
    "</HTML>\n";
    
close($fhout);

print "--- output in $fileOut\n" if (-e $fileOut);
exit;


