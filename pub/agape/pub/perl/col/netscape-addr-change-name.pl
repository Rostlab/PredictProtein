##!/usr/sbin/perl -w
#
$scrName=$0;$scrName=~s/^.*\/|\.pl//g;
$scrGoal="input: netscape-address-book, \n".
    "     action: changes field 'dn: cn=first family' to 'dn: cn=family, first'\t ";
#  
# 
#------------------------------------------------------------------------------#
#	Copyright				        	1999	       #
#	Burkhard Rost		rost@columbia.edu			       #
#	CUBIC (Columbia Univ)	http://www.columbia.edu/~rost/   	       #
#       Dept Biochemistry & Molecular Biophysics			       #
#       630 West, 168 Street						       #
#	New York, NY 10032	http://www.columbia.edu/~rost/                 #
#				version 0.1   	Sep,    	1999	       #
#------------------------------------------------------------------------------#
#

$[ =1 ;				# count from one

				# ------------------------------
				# defaults
%par=(
      '', "",			# 
      );
@kwd=sort (keys %par);
$Ldebug=0;
$Lverb= 0;
				# ------------------------------
if ($#ARGV<1){			# help
    print  "goal: $scrGoal\n";
    print  "use:  '$scrName '\n";
    print  "opt:  \n";
				#      'keyword'   'value'    'description'
    printf "%5s %-15s=%-20s %-s\n","","fileOut", "x",       "name of output file";
#    printf "%5s %-15s=%-20s %-s\n","","dirOut",  "x",       "directory of output files";
#    printf "%5s %-15s=%-20s %-s\n","","",   "x", "";
#    printf "%5s %-15s %-20s %-s\n","","",   "no value","";

    printf "%5s %-15s %-20s %-s\n","","dbg",   "no value",   "debug mode";
#    printf "%5s %-15s %-20s %-s\n","","noScreen", "no value","";
#    printf "%5s %-15s=%-20s %-s\n","","",   "x", "";
    if (defined %par && $#kwd > 0){
	$tmp= sprintf("%5s %-15s  %-20s %-s\n","","-" x 15 ,"-" x 20,"-" x 30);
	$tmp.=sprintf("%5s %-15s  %-20s %-s\n","","other:","default settings: "," ");
	$tmp2="";
	foreach $kwd (@kwd){
	    next if (! defined $par{$kwd} || length($par{$kwd})<1 );
	    if    ($par{$kwd}=~/^\d+$/){
		$tmp2.=sprintf("%5s %-15s= %10d %9s %-s\n","",$kwd,$par{$kwd}," ","(def)");}
	    elsif ($par{$kwd}=~/^[0-9\.]+$/){
		$tmp2.=sprintf("%5s %-15s= %10.3f %9s %-s\n","",$kwd,$par{$kwd}," ","(def)");}
	    else {
		$tmp2.=sprintf("%5s %-15s= %-20s %-s\n","",$kwd,$par{$kwd},"(def)");} 
	} 
	print $tmp, $tmp2       if (length($tmp2)>1);
    }
    exit;}
				# initialise variables
$fhin="FHIN";$fhout="FHOUT";
$#fileIn=0;
				# ------------------------------
				# read command line
foreach $arg (@ARGV){
#    next if ($arg eq $ARGV[1]);
    if    ($arg=~/^fileOut=(.*)$/)        { $fileOut=        $1;}
    elsif ($arg=~/^de?bu?g$/)             { $Ldebug=         1;
					    $Lverb=          1;}
#    elsif ($arg=~/^=(.*)$/){ $=$1;}
    elsif (-e $arg)                       { push(@fileIn,$arg); }
    else {
	$Lok=0; 
	if (defined %par && $#kwd>0) { 
	    foreach $kwd (@kwd){ 
		if ($arg =~ /^$kwd=(.+)$/){$Lok=1;$par{$kwd}=$1;
					   last;}}}
	if (! $Lok){ print "*** wrong command line arg '$arg'\n";
		     exit;}}}

$fileIn=$fileIn[1];
die ("missing input $fileIn\n") if (! -e $fileIn);
if (! defined $fileOut){
    $tmp=$fileIn;$tmp=~s/^.*\///g;$fileOut="Out-".$tmp;}

				# ------------------------------
				# (1) read file(s)
				# ------------------------------
print "--- $scrName: working on '$fileIn'\n";
open($fhin,$fileIn) || die "*** $scrName ERROR opening file $fileIn";
open($fhout,">".$fileOut) || die "*** $scrName ERROR creating file $fileOut";
while (<$fhin>) {
    $_=~s/\n//g;
    $line=$_;
				# not to be changed line
    if ($line !~/dn: cn=/ &&
	$line !~/cn: /){
	print $fhout $line,"\n";
	next;}
				# keep lists as they are
    if ($line=~/list/){
	print $fhout $line,"\n";
	next;}
				# ----------------------------------------
				# (1) 'dn: cn=first second,mail='
    if ($line =~/dn: cn=/){
	$before=$line; $before=~s/^(.*dn\: cn=).*$/$1/g;
	$after= $line; $after=~s/^.*dn\: cn=.+(,mail.*)$/$1/g;
	$name=  $line; $name=~s/^.*dn\: cn=(.+),mail.*$/$1/g;
				# change only if name not already with commata
	if ($name!~/\,/){
	    @tmp=split(/\s+/,$name);
	    $name=$tmp[$#tmp].",".join(' ',@tmp[1..($#tmp-1)]);
	    $line=$before.$name.$after;}
	print $fhout $line,"\n";
	print "xx 1: $line\n";
	next; }
				# ----------------------------------------
				# (2) 'cn: first second'
    if ($line =~/cn:/){
	$name=  $line; $name=~s/^.*cn\s*\:\s*(\S*.*)$/$1/;
				# change only if name not already with commata
	if ($name!~/\,/){
	    @tmp=split(/\s+/,$name);
	    $name=$tmp[$#tmp].",".join(' ',@tmp[1..($#tmp-1)]);
	    $line="cn: ".$name;}
	print "xx 2: $line\n";
	print $fhout $line,"\n";
	next; }
				# ERROR: unrecognised line
    print "*** ERROR $scrName: unk line=$line\n";
    exit;
}
close($fhin);
close($fhout);

print "--- output in $fileOut\n" if (-e $fileOut);
exit;


